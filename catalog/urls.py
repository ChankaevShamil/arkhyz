# arkhyz_life/catalog/urls.py

from django.urls import path
from . import views # Мы импортируем views из текущего приложения (catalog)

app_name = 'catalog' # Это пространство имен для URL-ов. Полезно для избежания конфликтов.

urlpatterns = [
    # 1. Главная страница (соответствует фото 1)
    path('', views.home_page, name='home'),

    # 2. Список Категорий услуг (соответствует фото 2)
    # Этот URL может быть вызван AJAX-запросом для бургер-меню
    # или быть отдельной страницей, если бургер-меню - это просто ссылка на нее.
    # Для простоты пока сделаем его как часть главной страницы или отдельным вызовом,
    # который возвращает HTML-фрагмент или рендерит полную страницу.
    # Для ТЗ, где бургер открывает список, этот URL может быть для страницы,
    # на которую ведет бургер-меню, если оно не модальное.
    # Если бургер-меню динамически загружает контент, этот URL может быть не нужен,
    # а категории будут передаваться в контекст каждой страницы.
    # Пока оставим вариант, что бургер-меню будет отображать категории, полученные в `base.html`.
    # Если нужна отдельная страница /categories/
    path('categories/', views.category_list_page, name='category_list_page'), # Назовем ее так для ясности

    # 3. Страница Списка Провайдеров в выбранной Категории (соответствует фото 3)
    # Используем slug категории для ЧПУ
    path('category/<slug:category_slug>/', views.provider_list_page, name='provider_list_page'),

    # 4. Детальная страница Провайдера (соответствует фото 4)
    # Используем slug провайдера для ЧПУ
    path('provider/<slug:provider_slug>/', views.provider_detail_page, name='provider_detail_page'),
]